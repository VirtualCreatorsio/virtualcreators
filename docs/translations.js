// Switch language function
function switchLanguage(lang) {
  console.log("Switching language to:", lang)
  currentLanguage = lang
  localStorage.setItem("preferred-language", lang)
  
  // Get current page path and determine the equivalent page in the other language
  const currentPath = window.location.pathname
  let newUrl
  
  // Handle local development vs production
  if (window.location.protocol === 'file:') {
    // Local development - construct relative path
    const pathParts = window.location.pathname.split('/')
    const docsIndex = pathParts.indexOf('docs')
    
    if (docsIndex !== -1) {
      // We're in the docs directory
      const basePath = pathParts.slice(0, docsIndex + 1).join('/')
      
              if (lang === 'nl') {
          // Switch to Dutch - check if we're already in a language subdirectory
          if (currentPath.includes('/nl/')) {
            // Already in Dutch, no change needed
            newUrl = currentPath
          } else if (currentPath.includes('/en/')) {
            // Switch from English to Dutch
            newUrl = currentPath.replace('/en/', '/nl/')
          } else {
            // From root to Dutch - preserve the full path structure
            const remainingPath = pathParts.slice(docsIndex + 1).join('/')
            newUrl = `${basePath}/nl/${remainingPath}`
          }
        } else {
          // Switch to English - check if we're already in a language subdirectory
          if (currentPath.includes('/en/')) {
            // Already in English, no change needed
            newUrl = currentPath
          } else if (currentPath.includes('/nl/')) {
            // Switch from Dutch to English - remove /nl/ and go to root
            const remainingPath = pathParts.slice(docsIndex + 2).join('/') // Skip 'nl' part
            newUrl = `${basePath}/${remainingPath}`
          } else {
            // From root to English (stay in root)
            newUrl = currentPath
          }
        }
    } else {
      // Fallback for other local paths
      const basePath = window.location.pathname.split('/').slice(0, -1).join('/')
      const currentPage = currentPath.split('/').pop() || 'index.html'
      
      if (lang === 'nl') {
        newUrl = `${basePath}/nl/${currentPage}`
      } else {
        newUrl = `${basePath}/${currentPage}`
      }
    }
  } else {
    // Production - use web URLs
    if (lang === 'nl') {
      // Switch to Dutch
      if (currentPath.includes('/nl/')) {
        // Already in Dutch, no change needed
        newUrl = currentPath
      } else if (currentPath.includes('/en/')) {
        // Switch from English to Dutch
        newUrl = currentPath.replace('/en/', '/nl/')
      } else {
        // From root to Dutch
        newUrl = `/nl${currentPath}`
      }
    } else {
      // Switch to English
      if (currentPath.includes('/en/')) {
        // Already in English, no change needed
        newUrl = currentPath
      } else if (currentPath.includes('/nl/')) {
        // Switch from Dutch to English - remove /nl/ and go to root
        const pathWithoutNl = currentPath.replace('/nl/', '/')
        // Special handling for homepage - ensure clean URL
        if (pathWithoutNl === '/index.html' || pathWithoutNl === '/') {
          newUrl = '/'
        } else {
          newUrl = pathWithoutNl
        }
      } else {
        // From root to English (stay in root)
        newUrl = currentPath
      }
    }
  }
  
  console.log("Navigating to:", newUrl)
  // Navigate to the new URL
  window.location.href = newUrl
}

