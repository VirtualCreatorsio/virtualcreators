// Check for language from URL path on page load
function checkLanguageFromURL() {
  const path = window.location.pathname
  
  // Check if we're in a language subdirectory
  if (path.includes('/nl/')) {
    currentLanguage = 'nl'
    localStorage.setItem("preferred-language", 'nl')
  } else if (path.includes('/en/')) {
    currentLanguage = 'en'
    localStorage.setItem("preferred-language", 'en')
  } else {
    // Root path - check if there's a stored preference, otherwise default to English
    const storedLang = localStorage.getItem("preferred-language")
    if (storedLang === 'nl') {
      currentLanguage = 'nl'
    } else {
      currentLanguage = 'en'
      // Only set English preference if none exists
      if (!storedLang) {
        localStorage.setItem("preferred-language", 'en')
      }
    }
  }
}

